    <script>
        $(function(){

            function smoothScroll(target){
                $('html, body').animate({
                    scrollTop: target.offset().top
                }, 1000);
            }
            
            $('#navbar-menu').find('a.custom-navbar-link').hover(function(){

                var hover = $(this).attr('data-hovered-link');
                
                $('#navbar-menu').find('span[data-verbose-target="'+hover+'"]').fadeToggle('fast');
            });

            $('#navbar-menu').find('a.custom-navbar-link').click(function(e){

                e.preventDefault();

                var clicked_data = $(this).attr('data-hovered-link');
                var target_data = $('#content-'+clicked_data);
                
                smoothScroll(target_data);
            });

            $('#content-chemication').find('span.scroller-link').click(function(){

                var clicked_data = $(this).attr('data-link-to');
                var target_data = $('#content-'+clicked_data);
                
                smoothScroll(target_data);
            });

            $('.button-to-site').click(function(){

                var id = $(this).attr('id');

                switch (id) {
                    case 'btn-ec':
                        location = '/ec';
                        break;
                    case 'btn-ceo':
                        location = '/ceo';
                        break;
                    case 'btn-hsfc':
                        location = '/hsfc';
                        break;
                }

            });

        });
